
public class DeclararArreglos {
	public static void main(String[] args) {
		
		// Formas en las que se pueden declarar arreglos
		
		// Forma 1: Declarar y luego inicializar
		int[] numeros;
		numeros = new int[5];
		
		// Forma 2: Declarar e inicializar en una sola linea
		String[] nombres = new String[3];
		
		// Forma 3: Declarar con valores iniciales
		String[] vocales = {"a", "e", "i", "o", "u"};
	}
}
